void Timer2A_Handler(void){
  TIMER2_ICR_R = TIMER_ICR_TATOCINT;    // acknowledge timer2A timeout
  PF2 ^= 0x04;         	// profile
	if (counter < numSamples){
		*(buffer + counter) = ADC_In();
	  counter++;
	}
	else{
		counter = 0;
		numSamples = 0;
		collectFinished = 0;				// finished sampling
		TIMER2_CTL_R = 0x00000000;		// disable timer2
	}
  //PF2 ^= 0x04;                   // profile
  PF2 ^= 0x04;                   // profile
}

// ***************** ADC_Open ****************
// Initialize ADC on specified channel
// Inputs:  ADC channel to initialize
// Outputs: none
int ADC_Open(uint32_t channelNum){
	
	SYSCTL_RCGCGPIO_R |= 0x20;            // activate port F
  ADC0_InitSWTriggerSeq3(channelNum);         // allow time to finish activating
  GPIO_PORTF_DIR_R |= 0x06;             // make PF2, PF1 out (built-in LED)
  GPIO_PORTF_AFSEL_R &= ~0x06;          // disable alt funct on PF2, PF1
  GPIO_PORTF_DEN_R |= 0x06;             // enable digital I/O on PF2, PF1
                                        // configure PF2 as GPIO
  GPIO_PORTF_PCTL_R = (GPIO_PORTF_PCTL_R&0xFFFFF00F)+0x00000000;
  GPIO_PORTF_AMSEL_R = 0;               // disable analog functionality on PF
  PF2 = 0;                      // turn off LED
	return 0;
}

// ***************** ADC_Open ****************
// CollectADC on specified channel
// Inputs:  
// Outputs: none
 void ADC_Collect(int channel_num, int sampling_rate, int* data_buffer, int num_samples){
	collectFinished = 1;
	ADC_Open(channel_num);
	Timer2_Init(sampling_rate);               	// set up Timer2A for interrupts, period specified by sampling_rate. FIX THIS!!!
	buffer = data_buffer;
	numSamples = num_samples;
	EnableInterrupts();
}
 
// ***************** ADC_Status ****************
// Return status of ADC_Collect
// Inputs:  none
// Outputs: 0 if ADC_Collect done, 1 if still waiting
int ADC_Status(){
	return collectFinished;
}



